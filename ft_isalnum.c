#include "libft.h"

int	ft_isalnum(int s)
{
	if (s < '0')
		return (0);
	if (s > '9' && s < 'A')
		return (0);
	if (s > 'Z' && s < 'a')
		return (0);
	if (s > 'z')
		return (0);
	return (1);
}
